package data;

public class Maker {
    private int id;
    private String name;

    // 기본 생성자
    public Maker() {
    }

    // id에 대한 getter와 setter
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    // name에 대한 getter와 setter
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
